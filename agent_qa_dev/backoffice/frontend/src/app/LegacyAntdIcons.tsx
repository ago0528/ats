function toReactAttrName(name: string) {
  if (!name.includes('-')) return name;
  return name.replace(/-([a-z])/g, (_, char: string) => char.toUpperCase());
}

function normalizeAttributes(attrs: Record<string, string | undefined> | undefined) {
  const result: Record<string, string> = {};
  if (!attrs) return result;
  Object.entries(attrs).forEach(([name, value]) => {
    if (typeof value === 'string') {
      result[toReactAttrName(name)] = value;
    }
  });
  return result;
}

function renderNode(node: AbstractNode): ReactNode {
  if (!node.children || node.children.length === 0) {
    return createElement(node.tag, { ...normalizeAttributes(node.attrs) });
  }

  return createElement(
    node.tag,
    {
      ...normalizeAttributes(node.attrs),
      fill: 'currentColor',
    },
    ...(node.children || []).map((child) => renderNode(child)),
  ) as ReactNode;
}

function makeIcon(def: IconDefinition): FC<IconProps> {
  return ({ className, style, title, spin }) => {
    const mergedStyle: CSSProperties = {
      ...iconContainer,
      ...(spin ? spinStyle : {}),
      ...(style ?? {}),
    };
    const svgProps = {
      ...normalizeAttributes(def.icon.attrs),
      width: '1em',
      height: '1em',
      fill: 'currentColor',
    };

    return (
      <span className={className} style={mergedStyle} title={title} aria-hidden="true">
        {createElement(
          'svg',
          {
            ...svgProps,
            style: {
              width: '100%',
              height: '100%',
            },
          },
          ...def.icon.children.map((node) => renderNode(node)),
        )}
      </span>
    );
  };
}

